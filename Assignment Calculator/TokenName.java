public enum TokenName
{       SEMC,    // ;
        LPAR,    // (
        RPAR,    // )
        MUL,     // *
        QUO,     // /
        REM,     // %
        PLUS,    // +
        MINUS,   // -
        ASSIGN,  // :=
        NUM,     // [0-9]+
        VAR,     // [a-zA-Z][a-zA-Z0-9]*
        UNDEF,   // undefined tokens
}